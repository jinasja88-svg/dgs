/**
 * 중국어 텍스트를 룩업 테이블로 한국어로 변환
 * @returns 번역된 문자열 또는 null (룩업 실패 시)
 */
export function lookupZhToKo(text: string): string | null {
  const trimmed = text.trim();

  // 완전 일치
  if (ZH_TO_KO[trimmed]) return ZH_TO_KO[trimmed];

  // '/' 로 구분된 복합 속성값 (예: "黑色/XL")
  if (trimmed.includes('/')) {
    const parts = trimmed.split('/');
    const translated = parts.map((p) => ZH_TO_KO[p.trim()] ?? p.trim());
    const hasTranslation = translated.some((_, i) => ZH_TO_KO[parts[i].trim()] !== undefined);
    if (hasTranslation) return translated.join(' / ');
  }

  // SKU/속성값에서 자주 보이는 복합 구분자
  for (const delimiter of [';', '；', ',', '，', '+']) {
    if (trimmed.includes(delimiter)) {
      const parts = trimmed.split(delimiter);
      const translated = parts.map((p) => ZH_TO_KO[p.trim()] ?? p.trim());
      const hasTranslation = translated.some((_, i) => ZH_TO_KO[parts[i].trim()] !== undefined);
      if (hasTranslation && translated.every((p) => !containsChinese(p))) {
        return translated.join(delimiter === '+' ? ' + ' : ' / ');
      }
    }
  }

  const phraseKeys = Object.keys(ZH_TO_KO)
    .filter((key) => key.length >= 2)
    .sort((a, b) => b.length - a.length);
  let replaced = trimmed;
  let changed = false;
  for (const key of phraseKeys) {
    if (replaced.includes(key)) {
      replaced = replaced.split(key).join(ZH_TO_KO[key]);
      changed = true;
    }
  }
  if (changed && !containsChinese(replaced)) return replaced;

  return null;
}

export function getEcommerceGlossaryPrompt(): string {
  return PROMPT_GLOSSARY.map(([zh, ko]) => `${zh}=${ko}`).join(', ');
}

/**
 * 해당 텍스트가 중국어를 포함하는지 확인
 */
export function containsChinese(text: string): boolean {
  return /[\u4e00-\u9fff]/.test(text);
}

/**
 * 해당 텍스트가 한국어를 포함하는지 확인
 */
export function containsKorean(text: string): boolean {
  return /[가-힣]/.test(text);
}
